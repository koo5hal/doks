import React from 'react';
import { Route } from 'react-router-dom';
import { useDispatch, useSelector } from 'react-redux';
import { LinkContainer } from 'react-router-bootstrap';
import { Navbar, Nav, Container, NavDropdown } from 'react-bootstrap';
import SearchBox from './SearchBox';
import { logout } from '../actions/userActions';

const Header = () => {
  const dispatch = useDispatch();

  const userLogin = useSelector((state) => state.userLogin);
  const { userInfo } = userLogin;

  const logoutHandler = () => {
    dispatch(logout());
  };

  return (
    <header>
      <Navbar
        style={{ backgroundColor: '#aad5d5' }}
        expand='lg'
        collapseOnSelect
      >
        <Container>
          <LinkContainer to='/'>
            <strong style={{ backgroundColor: '#aad5d5' }}>
              {' '}
              <Navbar.Brand>MoDokter</Navbar.Brand>
            </strong>
          </LinkContainer>
          <Navbar.Toggle aria-controls='basic-navbar-nav' />
          <Navbar.Collapse id='basic-navbar-nav'>
            <Route render={({ history }) => <SearchBox history={history} />} />
            <Nav className='ml-auto'>
              <LinkContainer to='/saved'>
                <Nav.Link>Saved Doctors</Nav.Link>
              </LinkContainer>{' '}
              <LinkContainer to='/patients'>
                <Nav.Link>Patients</Nav.Link>
              </LinkContainer>
              {userInfo ? (
                <NavDropdown title={userInfo.name} id='username'>
                  <LinkContainer to='/profile'>
                    <NavDropdown.Item>My Appointments</NavDropdown.Item>
                  </LinkContainer>
                  <LinkContainer to='/updateprofile'>
                    <NavDropdown.Item>Update Profile</NavDropdown.Item>
                  </LinkContainer>
                  <NavDropdown.Item onClick={logoutHandler}>
                    Logout
                  </NavDropdown.Item>
                </NavDropdown>
              ) : (
                <LinkContainer to='/login'>
                  <Nav.Link>
                    <i className='fas fa-user'></i> Sign In
                  </Nav.Link>
                </LinkContainer>
              )}
              {userInfo && userInfo.isAdmin && (
                <NavDropdown title='Admin' id='adminmenu'>
                  <LinkContainer to='/addPatientReport'>
                    <NavDropdown.Item>Add Patients Report</NavDropdown.Item>
                  </LinkContainer>
                  <LinkContainer to='/admin/userlist'>
                    <NavDropdown.Item>Users</NavDropdown.Item>
                  </LinkContainer>
                  <LinkContainer to='/admin/appointmentlist'>
                    <NavDropdown.Item>Doctor Profiles</NavDropdown.Item>
                  </LinkContainer>
                  <LinkContainer to='/admin/booklist'>
                    <NavDropdown.Item>My Patients</NavDropdown.Item>
                  </LinkContainer>
                </NavDropdown>
              )}
            </Nav>
          </Navbar.Collapse>
        </Container>
      </Navbar>
    </header>
  );
};

export default Header;
